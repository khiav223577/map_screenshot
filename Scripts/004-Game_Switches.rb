#==============================================================================
# ■ Game_Switches
#------------------------------------------------------------------------------
# 處理開關的類別。編入的是 Array 類別的外觀。本類的實例請參考 $game_switches。
#==============================================================================

class Game_Switches
  #--------------------------------------------------------------------------
  # ● 初始化目標
  #--------------------------------------------------------------------------
  def initialize
    @data = []
  end
  #--------------------------------------------------------------------------
  # ● 取得開關
  #     switch_id : 開關 ID
  #--------------------------------------------------------------------------
  def [](switch_id)
    if switch_id <= 5000 and @data[switch_id] != nil
      return @data[switch_id]
    else
      return false
    end
  end
  #--------------------------------------------------------------------------
  # ● 設定開關
  #     switch_id : 開關 ID
  #     value     : ON (true) / OFF (false)
  #--------------------------------------------------------------------------
  def []=(switch_id, value)
    if switch_id <= 5000
      @data[switch_id] = value
    end
  end
end
