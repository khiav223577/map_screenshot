#==============================================================================
# ■ Game_Variables
#------------------------------------------------------------------------------
# 處理變數的類別。編入的是 Array 類別的外觀。
# 本類別的實例請參考 $game_variables。
#==============================================================================

class Game_Variables
  #--------------------------------------------------------------------------
  # ● 初始化
  #--------------------------------------------------------------------------
  def initialize
    @data = []
  end
  #--------------------------------------------------------------------------
  # ● 取得變數
  #     variable_id : 變數 ID
  #--------------------------------------------------------------------------
  def [](variable_id)
    if variable_id <= 5000 and @data[variable_id] != nil
      return @data[variable_id]
    else
      return 0
    end
  end
  #--------------------------------------------------------------------------
  # ● 設定變數
  #     variable_id : 變數 ID
  #     value       : 變數的值
  #--------------------------------------------------------------------------
  def []=(variable_id, value)
    if variable_id <= 5000
      @data[variable_id] = value
    end
  end
end
